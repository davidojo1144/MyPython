#since one pound is 0.454 kilogram
#i prompted the user to give me a value in pounds
#to convert the user given pounds to kilograms, i multiplied the user given value in pounds with my given kilogram which is 0.454 kilogram
#then i use my print function to print the given result out.

user = int(input("Enter a value in pounds: "))
one_pound_in_kilograms = 0.454
result = user * one_pound_in_kilograms 
print(result)