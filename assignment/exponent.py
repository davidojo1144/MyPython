large_exponent = 10**6
very_large_exponent = 10**8

number1 = 2 ** large_exponent
number2 = 2 ** very_large_exponent


print(f"2 raised to the power of {large_exponent} is a number with {str(number1)} digits.")
print(f"2 raised to the power of {very_large_exponent} is a number with {str(number2)} digits.")